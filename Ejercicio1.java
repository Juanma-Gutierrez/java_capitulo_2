public class Ejercicio1 {
    public static void main(String[] args) {
        int x;
        int y;
        x = 144;
        y = 999;
        System.out.println(x + " + " + y + " = " + (x + y));
        System.out.println(x + " - " + y + " = " + (x - y));
        System.out.println(x + " * " + y + " = " + (x * y));
        System.out.println(x + " / " + y + " = " + ((float)x / (float)y));
    }
}